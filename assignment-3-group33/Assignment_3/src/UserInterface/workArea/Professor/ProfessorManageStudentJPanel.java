    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if((txtStudentId.getText()).equals("")){
           JOptionPane.showMessageDialog(null,"Please enter Student Id to search");
           return;   
        }
        int studentId = Integer.parseInt(txtStudentId.getText());
        System.out.println(studentId);
        StudentProfile student = studentList.findStudent(studentId);
        if(student == null){
           JOptionPane.showMessageDialog(null,"No student with student Id: "+studentId+" found");
           return;  
        }
        System.out.println(student);
        this.studentCourses = studentCourseList.findStudentCourses(student);
        populateStudentTable(studentCourses);
        
    }//GEN-LAST:event_btnSearchActionPerformed

