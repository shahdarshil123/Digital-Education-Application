    private void cbCourseStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbCourseStatusActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblStudentStatus.getSelectedRow();
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this,"Please select row from Course Catalog to delete");
            return;
        }
        DefaultTableModel dtm = (DefaultTableModel) tblStudentStatus.getModel();
        //String status= (String) dtm.getValueAt(selectedRowIndex, 3);
        StudentCourseStatus studentCourseStatus = (StudentCourseStatus) dtm.getValueAt(selectedRowIndex, 3);
        System.out.println(studentCourseStatus.getCourseStatus());
        String status = cbCourseStatus.getSelectedItem().toString();
        System.out.println(status);
        studentCourseStatus.setCourseStatus(status);
        populateStudentTable(studentCourses);
        
      
    }//GEN-LAST:event_cbCourseStatusActionPerformed

    private void txtStudentIdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtStudentIdKeyPressed
        // TODO add your handling code here:
        int digit = evt.getKeyChar();
        if(!((digit>='0' && digit <= '9')||evt.getExtendedKeyCode()== KeyEvent.VK_BACK_SPACE)){
            JOptionPane.showMessageDialog(null,"Please enter only digits");      
        }
    }//GEN-LAST:event_txtStudentIdKeyPressed
  public void populateStudentTable(StudentCourses studentCourses){
       DefaultTableModel dtm = (DefaultTableModel) tblStudentStatus.getModel();
       dtm.setRowCount(0);
       for(StudentCourseStatus studentCourseStatus: studentCourses.getStudentCourses()){
           if(studentCourseStatus.getStudentBrowseCourses().getProfessor() == userAccount.getProfessorProfile()){
                Object row[]= new Object[4];
                row[0] = studentCourses.getStudent().getPerson().getPersonName();
                row[1] = studentCourseStatus.getStudentBrowseCourses();
                row[2] = studentCourseStatus.getStudentBrowseCourses().getCourseSemester().getCourse().getCourseName();
                row[3] = studentCourseStatus;
                dtm.addRow(row);
           }         
       }
}

