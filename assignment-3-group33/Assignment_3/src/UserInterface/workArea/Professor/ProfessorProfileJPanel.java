    public void populateDetails(){
        calculateRating();
        txtProfessorName.setText(userAccount.getProfessorProfile().getPerson().getPersonName());
        txtProfessorID.setText(Integer.toString(userAccount.getProfessorProfile().getPerson().getPersonId()));
        txtrRating.setText(Double.toString(userAccount.getProfessorProfile().getRating()));
        cbSpecialization.setSelectedItem(userAccount.getProfessorProfile().getSpecialization());
        cbRegion.setSelectedItem(userAccount.getProfessorProfile().getRegion());
        cbLanguage.setSelectedItem(userAccount.getProfessorProfile().getLanguage());
        txtUsername.setText(userAccount.getUsername());
        txtPassword.setText(userAccount.getPassword());
    }
    
    public void calculateRating(){
        double rating = 0;
        int counter = 1;
         StudentCourseDirectory studentCourseList = business.getStudentCoursesList();
         for(StudentCourses studentCourses: studentCourseList.getStudentCourseList()){
             for(StudentCourseStatus studentCourseStatus:studentCourses.getStudentCourses()){
                 if(studentCourseStatus.getStudentBrowseCourses().getProfessor() == userAccount.getProfessorProfile()){
                     rating += studentCourseStatus.getProffesorRating();
                     counter += 1;
                 }
             }
         }
         rating = (rating/counter);
         userAccount.getProfessorProfile().setRating(rating);
    }
