    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        btnUpdate.setEnabled(true);
        
        txtPassword.setEnabled(false);
        btnSave.setEnabled(false);
        cbSpecialization.setEnabled(false);
        cbRegion.setEnabled(false);
        cbLanguage.setEnabled(false);
        
        String password =txtPassword.getText();
        if(password.equals("")){
           JOptionPane.showMessageDialog(null,"Please enter password");
           populateDetails();
           return;             
        }
        userAccount.setPassword(password);
        if(cbRegion.getSelectedItem() == null){
           JOptionPane.showMessageDialog(null,"Please select Region from list");
           populateDetails();
           return;            
        }
        userAccount.getProfessorProfile().setRegion(cbRegion.getSelectedItem().toString());
        if(cbSpecialization.getSelectedItem() == null){
          JOptionPane.showMessageDialog(null,"Please select specializatoin from list");
          populateDetails();
          return;  
        }
        userAccount.getProfessorProfile().setSpecialization(cbSpecialization.getSelectedItem().toString());
        if(cbLanguage.getSelectedItem() == null){
          JOptionPane.showMessageDialog(null,"Please select Language from list");
          populateDetails();
          return;  
        }        
        userAccount.getProfessorProfile().setLanguage(cbLanguage.getSelectedItem().toString());
        JOptionPane.showMessageDialog(null,"Account updated");
    }//GEN-LAST:event_btnSaveActionPerformed

